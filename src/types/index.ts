export interface PeriodStats {
  spend: number;
  impressions: number;
  clicks: number;
  conversions: number;
  grossRevenue: number;
  netRevenue: number;
  cm2: number;
  acm2: number;
  roas: number; // netRevenue / spend
  ctr: number;
}

export interface DailyDataPoint {
  date: string;
  spend: number;
  impressions: number;
  clicks: number;
  conversions: number;
  grossRevenue: number;
  netRevenue: number;   // = ROAS numerator (attributed revenue)
  cm2: number;          // Contribution Margin 2 = POAS numerator
  acm2: number;         // Attributed CM2       = aPOAS numerator
  ctr: number;
  cpc: number;
  cpm: number;
  roas: number;  // netRevenue  / spend
  poas: number;  // cm2         / spend
  apoas: number; // acm2        / spend
}

export interface Campaign {
  id: string;
  name: string;
  platform: string;
  spend: number;
  impressions: number;
  clicks: number;
  conversions: number;
  grossRevenue: number;
  netRevenue: number;
  cm2: number;
  acm2: number;
  roas: number; // netRevenue / spend
  ctr: number;
  cpc: number;
  cpm: number;
  dailyData: DailyDataPoint[];
  period: {
    current: PeriodStats;
    previous: PeriodStats;
  };
  bucket: BucketType;
  // Optional field, generated by AI
  aiStatus?: '🟢 Performing' | '🟡 Watch' | '🔴 Underperforming';
}

